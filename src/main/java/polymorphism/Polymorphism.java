package polymorphism;

public class Polymorphism {
	/***
	 * Polymorphism is like one thing having many forms
	 * Multiple methods with same name
	 * But parameters different w.r.t data type, number of parameter or order of parameters
	 * Also called method overloading
	 * Method overloading
	 * Constructor over loading
	 * Return type will not make any difference in overloading
	 * 
	 * Rules:
	 * 
	 * 1. Method name should be same
	 * 2. Number of parameter should be different
	 * 3. Data type of parameter should be different
	 * 4. Order of parameters should be different
	 * 
	 * Why Overloading required:
	 * 
	 * Code re-usability and maintainability
	 * 
	 */

}
