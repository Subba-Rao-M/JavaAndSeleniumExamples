package collections;

public class CollectionsDemo {
	
	/**
	 * Collections is a root interface
	 * Collections is extended into List and Set Interfaces
	 * List interface implemented by class ArrayList
	 * Set interface implemented by class HashSet
	 * Map interface is implemented by HashMap
	 * Above holds group of objects not primitive data
	 * To store primitive data we have to use wrapper classes
	 * collections is interface available in java.util
	 * 
	 * Array List: 
	 * 
	 * ArrayList is class which implements collection and List interface
	 * Heterogeneous data is allowed
	 * Insertion order (index) is preserved
	 * In indexing technique it will preserve the order and same position in maintained
	 * In hashing index is not preserved and data will be stored randomly 
	 * 
	 * There is no fixed size and it is dynamic as no need to specify size
	 * Array works with primitive data type and ArraysList works on objects
	 * Duplicate is allowed
	 * Multiple nulls allowed
	 * Null is not empty value, it is unknown value
	 * 
	 * 
	 * HASH Set or Set Collection 
	 * 
	 * HashSet is class which implements collection and Set interface
	 * Heterogeneous data is allowed
	 * Insertion order is not preserved i.e index is not supported
	 * Duplicate element is not allowed
	 * Multiple null is not allowed and only single null value is supported
	 * 
	 * 
	 * 
	 * HashMap :
	 * 
	 * Hashmap is derived from map interface
	 * Data is stored in key and value pair
	 * Data can be added, removed using key value pair
	 * If you have key value pair data, you can use hashmap
	 * Key is always unique and value can have duplicate
	 * Insertion order is not maintained i.e indexing is not supported
	 * All keys should be integer 
	 * 
	 * 
	 */

}
